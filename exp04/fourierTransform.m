figure;

img1 = imread("f1.png");
subplot(2,4,1);
imshow(img1);
xlabel('原图像1');
img1 = fft2(im2double(img1));
img1 = fftshift(img1);
img1 = abs(img1);
img1 = log(img1+1);
mmax = max(max(max(img1)));
mmin = min(min(min(img1)));
img1 = 255*img1./(mmax-mmin);
subplot(2,4,5);
imshow(uint8(img1));
xlabel('变换后图像1');

